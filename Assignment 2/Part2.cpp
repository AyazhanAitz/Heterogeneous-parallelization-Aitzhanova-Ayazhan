#include <iostream>   // Для ввода и вывода (cout, cin)
#include <cstdlib>    // Для функций rand() и srand()
#include <ctime>      // Для функции time()
#include <climits>    // Для констант INT_MIN и INT_MAX
#include <omp.h>      // Для работы с OpenMP

using namespace std;

int main() {
    // Устанавливаем русскую локаль для корректного вывода текста
    setlocale(LC_ALL, "Russian");

    // Размер массива
    const int N = 10000;

    // Динамически выделяем память под массив из N целых чисел
    int* arr = new int[N];

    // Инициализируем генератор случайных чисел текущим временем
    srand(static_cast<unsigned int>(time(nullptr)));

    // Заполняем массив случайными числами в диапазоне от 1 до 100000
    for (int i = 0; i < N; i++) {
        arr[i] = 1 + rand() % 100000;
    }

    // Переменные для последовательного поиска минимума и максимума
    int seq_min = INT_MAX;
    int seq_max = INT_MIN;

    // Запоминаем время начала последовательного алгоритма
    double start_seq = omp_get_wtime();

    // Последовательно проходим по массиву и ищем min и max
    for (int i = 0; i < N; i++) {
        if (arr[i] < seq_min) {
            seq_min = arr[i];
        }
        if (arr[i] > seq_max) {
            seq_max = arr[i];
        }
    }

    // Запоминаем время окончания последовательного алгоритма
    double end_seq = omp_get_wtime();

    // Время выполнения последовательной версии в миллисекундах
    double time_seq = (end_seq - start_seq) * 1000.0;

    // Переменные для параллельного поиска минимума и максимума
    int par_min = INT_MAX;
    int par_max = INT_MIN;

    // Запоминаем время начала параллельного алгоритма
    double start_par = omp_get_wtime();

    /*
      Директива #pragma omp parallel for распараллеливает цикл for.
      Массив делится между потоками, каждый поток обрабатывает свою часть.

      reduction(min:par_min) означает, что каждый поток находит свой локальный минимум,
      а затем все локальные минимумы объединяются в один общий par_min.

      reduction(max:par_max) работает аналогично для максимального значения.
    */
#pragma omp parallel for reduction(min:par_min) reduction(max:par_max)
    for (int i = 0; i < N; i++) {
        if (arr[i] < par_min) {
            par_min = arr[i];
        }
        if (arr[i] > par_max) {
            par_max = arr[i];
        }
    }

    // Запоминаем время окончания параллельного алгоритма
    double end_par = omp_get_wtime();

    // Время выполнения параллельной версии в миллисекундах
    double time_par = (end_par - start_par) * 1000.0;

    // Выводим результаты работы программы
    cout << "Размер массива: " << N << " элементов\n\n";

    cout << "Последовательная реализация:\n";
    cout << "  Минимальное значение: " << seq_min << "\n";
    cout << "  Максимальное значение: " << seq_max << "\n";
    cout << "  Время выполнения: " << time_seq << " мс\n\n";

    cout << "Параллельная реализация (OpenMP):\n";
    cout << "  Минимальное значение: " << par_min << "\n";
    cout << "  Максимальное значение: " << par_max << "\n";
    cout << "  Время выполнения: " << time_par << " мс\n\n";

    // Формируем вывод по результатам сравнения
    cout << "Вывод:\n";

    if (time_par < time_seq) {
        cout << "  Параллельная реализация работает быстрее,\n";
        cout << "  так как используется несколько потоков процессора.\n";
    } else if (time_par > time_seq) {
        cout << "  Последовательная реализация оказалась быстрее,\n";
        cout << "  поскольку для массива из 10 000 элементов\n";
        cout << "  накладные расходы на создание потоков и синхронизацию\n";
        cout << "  превышают выигрыш от параллельных вычислений.\n";
    } else {
        cout << "  Время выполнения обеих реализаций примерно одинаково.\n";
    }

    // Освобождаем динамически выделенную память
    delete[] arr;

    return 0;
}
